package main

import "fmt"

func main() {
	// variable use for Println
	var name = "SUFIYAN SHAIKH"
	fmt.Println(name)

	var age = 18
	fmt.Println(age)

	var height = 5.7342
	fmt.Println(height)

	//variable use for Printf

	fmt.Printf("Name: %s\n", name)
	fmt.Printf("Age: %d\n", age)
	fmt.Printf("Height: %.2f\n", height)
}
